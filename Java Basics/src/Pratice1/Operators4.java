package Pratice1;

public class Operators4 {
    public static void main(String[] args){
        int i=20;
        int j=10;
        //conditional operators
        //&&(conditional AND)
        //||(conditional OR)
        System.out.println(i<j || i>j);
        System.out.println(i!=j && i<j);


       /*
       for  && operator
       true-true=true
       true-false=false
       false-true=false
       false-false=false
        */

    }
}
